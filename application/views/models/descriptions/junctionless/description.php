<p>
    Junctionless transistor is considered as a promising device to extend the limit of traditional MOSFETs due to its simple fabrication, no abrupt junction technology restrict and superior performance. The Junctionless model here is a potential-based compact
    model for double-gate Junctionless Transistor (DGJT), which is valid in all operation regimes for a wide device parameters. While due to the lack of capacitance model, the AC simulation is not available yet.
</p>
