<p>
    This is a surface potential based model for a typical AlGaN HEMTs. An accurate solution from weak inversion, through moderate inversion and finally to strong inversion region is found for the one dimensional (1D) Poisson equation and the Schrodinger equation
    in the triangular potential. Velocity saturation, channel length modulation, drain-induced barrier lower effect, and self-heating effect are included in the presented model.
</p>
