<p>
    Tunneling field-effect-transistors (TFETs) which break the subthreshold swing limit of 60mV/dec in MOSFETs are possible solutions to the power crisis of CMOS. The TFET model here is a surface-potential-based compact model for double-gate (DG) TFETs. Descriptions
    of both the current-voltage and charge-voltage characteristics are included in this model.
</p>
