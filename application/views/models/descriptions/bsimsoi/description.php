<p>
    BSIMSOI is a compact model for SOI (Silicon-On-Insulator) MOSFET circuit design. It is one of the BSIM series models by the UC Berkeley BSIM Research Group. In December 2001, BSIMSOI is selected by the CMC (Compact Model Council) as the standard SOI MOSFET
    model. A detailed introduction and technical manual can be found on the BSIM Research Group webpage [2].
</p>
