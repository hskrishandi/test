<p>
    BSIM4 is a physics-based bulk MOSFET model for sub-100nm nodes. It is one of the BSIM series models by the UC Berkeley BSIM Research Group firstly released in 2000 and then approved by the CMC (Compact Model Council) as an open standard model. It has
    been widely used in the semiconductor industry from 0.13 um to 22 nm technology nodes. A detailed introduction and technical manual can be found on the BSIM Research Group webpage [2].
</p>
