<p>
    HiSIM (Hiroshima-university STARC IGFET Model) is the first complete surface-potential-based MOSFET model for circuit simulation based on the drift-diffusion approximation. The most important advantage of the surface-potential-based modeling is the united
    description of device characteristics for all bias conditions. Up to now validity of HiSIM has been tested for channel lengths down to 45nm with CMOS fabrication technologies using the pocket-implant technology. A detailed introduction and technical
    manual can be found on the HiSIM Research Center website [2].
</p>
