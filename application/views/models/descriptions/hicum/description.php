<p>
    HICUM (HIgh-CUrrent Model) is a compact model for bipolar transistor with semi-physical nature. It is emphasized especially at the operating region of high current densities. Verified for a variety of techniques of Si, SiGe, and III-V, HICUM is found
    to be accurate for high power transistors with BV_(CEO) values up to 15V. It has been selected as one of the standard bipolar transistor models by the CMC in 2003.
</p>
