<h4>Introduction</h4>			
<div class="details">
	<div class="structure-figure">
		<img src="<?php echo base_url('images/simulation/otft.png');?>" />
	</div>			
	<p>The Unified Model and Parameter Extraction Method (UMEM) developed for a-Si:H thin-film transistors, TFTs [1] complemented with specific features of organic TFTs as high conductivity due to involuntary doping, series and contact resistance. Drain current is modeled in the above and below threshold regimes. All above threshold parameters are extracted from two transfer characteristics, (one in the linear and the other in the saturation region) and from the output characteristic of the device under study [1,2]. The extraction procedure explained in [2], can be implemented in any program for mathematical calculations. Other specific feature for OTFTs that can be analyzed can be seen in [3-4].
	</p>
