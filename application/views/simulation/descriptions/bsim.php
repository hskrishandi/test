<div class="details">			
	<p>
		BSIM3 is a physics-based, deep-submicron MOSFET model for digital and analog circuit designs. BSIM3,version 3.1, was released by Device Group at the University of California at Berkeley in 1995. It provided new advancements based on its previous releases.Meticulous care has been taken to mesh the above model enhancements with high levels of accuracy and minimum simulation costs. In addition, the enhanced expressions yield more continuous behavior and should also help to facilitate faster SPICE convergence properties. A detailed introduction and technical manual can be found on the BSIM Research Group website [2].
